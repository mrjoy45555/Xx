    const now = new Date(Date.now() + 6 * 60 * 60 * 1000); // +6 GMT (Bangladesh time)
    const currentTime = now.toLocaleTimeString('en-US', { hour12: true });
    const found = nam.find(item => item.timer === currentTime);
    if (found && global.GoatBot && Array.isArray(global.GoatBot.allThreadID)) {
      const message = found.message[Math.floor(Math.random() * found.message.length)];
      global.GoatBot.allThreadID.forEach(threadID => {
        api.sendMessage(message, threadID);
      });
    }
